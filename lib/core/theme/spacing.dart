/// Consistent spacing values used throughout the app.
///
/// Based on a 4px grid system for visual rhythm.
class Spacing {
  Spacing._();

  /// 4px - Minimal spacing
  static const double xs = 4;

  /// 8px - Tight spacing (between related elements)
  static const double sm = 8;

  /// 12px - Compact spacing
  static const double md = 12;

  /// 16px - Standard spacing (card padding, section gaps)
  static const double lg = 16;

  /// 20px - Comfortable spacing
  static const double xl = 20;

  /// 24px - Generous spacing (major sections)
  static const double xxl = 24;

  /// 32px - Large gaps
  static const double xxxl = 32;

  /// Standard card internal padding
  static const double cardPadding = 16;

  /// Standard page horizontal padding
  static const double pagePadding = 16;

  /// Standard list item vertical spacing
  static const double listItemSpacing = 6;

  /// Space to leave at bottom for FAB clearance
  static const double fabClearance = 80;
}
